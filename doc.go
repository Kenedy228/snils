// Package snils предоставляет функции для нормализации и валидации
// страхового номера индивидуального лицевого счета (СНИЛС) Российской Федерации.
//
// Пакет реализует:
//   - приведение СНИЛС к нормализованному виду (удаление пробелов и разделителей);
//   - проверку длины и допустимых символов;
//   - проверку на запрещенные значения;
//   - проверку контрольного числа по установленному алгоритму.
//
// Нормализованный СНИЛС представляет собой строку из 11 цифр без пробелов
// и разделителей (например, "11223344595").
//
// Основные функции:
//
//   - Normalize — удаляет пробелы и разделители из строки СНИЛС;
//   - Validate — выполняет полную проверку нормализованного СНИЛС.
//
// Пример использования:
//
//	s := snils.Normalize("112-233-445 95")
//
//	if err := snils.Validate(s); err != nil {
//	    // обработка ошибки
//	}
//
//	if err := snils.Validate("11223344595"); err == nil {
//	    // СНИЛС валиден
//	}
//
// Проверка контрольного числа выполняется только для номеров,
// превышающих установленный порог (см. MinNumberForChecksum).
package snils
